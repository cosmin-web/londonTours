		<h1>Our History</h1>
		<p>It all began in the 1930s when Mr Evan Evans, a former Mayor of the London Borough of St Pancras, started with a few small coaches offering offering escorted 'charabanc' tours of London.</p>
		<p>In those days the average price of a full day tour was 28 shillings (approximately 1 US Dollar) - a tour to Stratford-upon-Avon would take up to 14 hours. In 1930 a tour of London would have included a visit to The People’s Palace; opened in 1887 it was built to provide cultural activities for the poor people of the East End of London.</p>
		<p>Many things have changed over the past 85 years – we now have faster, safer coaches, with Wi-Fi and air conditioning and thanks to our modern motorways a tour to Stratford upon Avon now takes around 10 hours. However, what visitors to the UK want to see has not changed; Stratford-upon-Avon remains one of our most popular tours.</p>
